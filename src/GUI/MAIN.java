    private void jButtonthemtour3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonthemtour3ActionPerformed
        // TODO add your handling code here:
        Date date1 = jDateChooserngaybdtour.getDate();
        Date date2 = jDateChooserngaykttour.getDate();
        String pattern = "yyyy-MM-dd";
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat(pattern);
        String ngaybd = simpleDateFormat.format(date1);
        String ngaykt = simpleDateFormat.format(date2);
        if (jTextFieldgiatour.getText().isEmpty()) {
            ThongBao.error("Tên không để trống");
        } else if (date2.before(date1)) {
            ThongBao.error("Ngày bắt đầu không lớn hơn ngày kết thúc");
        } else {
            int magia = Integer.valueOf(jTextFieldmagia.getText());
            SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            int tour = Rename.matour[jComboBoxtour2.getSelectedIndex()];
            int gia = Integer.parseInt(jTextFieldgiatour.getText());
            giaBUS.edit(magia, tour, ngaybd, ngaykt, gia);
            dBUS.updateDoanhthuTour(tour);
            
            try {
                
                dBUS.updateTable(jTableDsdoan);
                ThongBao.noitice("sửa thành công");
                giaBUS.updateTable(jTableDsgia);
                jTableDsgia.removeColumn(jTableDsgia.getColumnModel().getColumn(0));
                jTextFieldmatour.setText(null);
                jTextFieldgiatour.setText(null);
                jComboBoxtour2.setSelectedIndex(0);

            } catch (Exception ex) {
                Logger.getLogger(MAIN.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonthemtour3ActionPerformed

