package Tools;
public class Swap {

    String ten;
    int ma;

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getMa() {
        return ma;
    }

    public void setMa(int ma) {
        this.ma = ma;
    }

    public Swap(String ten, int ma) {
        this.ten = ten;
        this.ma = ma;
    }
}

